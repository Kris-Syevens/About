public class Day18 {
    // Notes

    // DESCRIPTION:
    // Write a function which converts the input string to uppercase.

    public static String uppercaseMe(String s) {

        return s.toUpperCase();

    }

    // Testing notes below:
    public static void main(String[] args) {

        System.out.println(uppercaseMe("Kristofer")); // KRISTOFER
        System.out.println(uppercaseMe("Hello World")); // HELLO WORLD

    }

}
